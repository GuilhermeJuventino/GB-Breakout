SECTION "header", ROM0[$100]
  jp EntryPoint

  ds $150 - @, 0 ; Make room for header


EntryPoint:
  ; Do not turn off LCD outside of VBlank
WaitVBlank:
  ld a, [rLY]
  cp 144
  jp c, WaitVBlank

  ; Turn off LCD
  ld a, 0
  ld [rLCDC], a

  ; Copy tile data
  ld de, Tiles
  ld hl, $9000 ; Location of tiles in VRAM
  ld bc, TilesEnd - Tiles ; Length of Tiles
  call Memcpy

  ; Copy TileMap
  ld de, Tilemap
  ld hl, $9800
  ld bc, TilemapEnd - Tilemap
  call Memcpy

  ; Copy the paddle tile
  ld de, Paddle
  ld hl, $8000
  ld bc, PaddleEnd - Paddle
  call Memcpy

  ; Clear OAM (Object Attribute Memory)
  ld a, 0
  ld b, 160
  ld hl, _OAMRAM
ClearOam:
  ld [hli], a
  dec b
  jp nz, ClearOam

  ; Write object properties to OAM
  ld hl, _OAMRAM
  ld a, 128 + 16 ; Object's Y coordinate
  ld [hli], a
  ld a, 16 + 8 ; Object's X Coordinate
  ld [hli], a
  ld a, 0 ; Ojbect's Tile ID and attributes
  ld [hli], a
  ld [hli], a

  ; Initializing Global Variables
  ld a, 0
  ld [wFrameCounter], a
  
  ; OBS: Every operation that involves copying and moving tiles or writting stuff to the OAM
  ; needs to be performed BEFORE TURNING THE LCD ON! Otherwise, changes will be ignored
  ; and glitches can happen

  ; Turn LCD on
  ld a, LCDCF_ON | LCDCF_BGON | LCDCF_OBJON
  ld [rLCDC], a

  ; Initialize display registers during the first blank frame
  ld a, %11100100
  ld [rBGP], a
  ld a, %11100100
  ld [rOBP0], a

Main:
  ; Main loop/entrypoint. A.K.A. our "Main() function"
  ; Wait until it's *NOT* VBlank
  ld a, [rLY]
  cp 144
  jp nc, Main
WaitVBlank2:
  ld a, [rLY]
  cp 144
  jp c, WaitVBlank2

  ld a, [wFrameCounter]
  inc a
  ld [wFrameCounter], a
  cp a, 15 ; Run the following code every 15 frames
  jp nz, Main

  ; Set the frame counter back to zero
  ld a, 0
  ld [wFrameCounter], a

  ; Move the paddle
  ld a, [_OAMRAM + 1]
  inc a
  ld [_OAMRAM + 1], a
  jp Main

